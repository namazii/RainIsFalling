import UIKit

enum Colors {
    enum BackGround {
        static let primary: UIColor = UIColor(resource: .colorsBackGroundPrimary)
        static let secondary: UIColor = UIColor(resource: .colorsBackGroundSecondary)
    }
    
    enum Content {
        static let primary: UIColor = UIColor(resource: .colorsContentPrimary)
        static let secondary: UIColor = UIColor(resource: .colorsContentSecondary)
    }
}
